
public class CharTest {

	public static void main(String[] args) {

		//문자 리터럴
		char c = 'A';
		char c2 = '홍';
		// escape 문자
		System.out.println("helloworld");
		System.out.println("hello\tworld"); // 키보드 tab 친 효과
		System.out.println("hello\nworld"); // 키보드 enter 친 효과
		System.out.println("hello\'world");  //  ' 출력
		System.out.println("hello\"world");  //  " 출력
		System.out.println("hello\\world");  //   \ 출력
	}

}
